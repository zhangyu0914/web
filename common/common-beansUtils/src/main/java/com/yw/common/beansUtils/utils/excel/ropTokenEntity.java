package com.yw.common.beansUtils.utils.excel;


public class ropTokenEntity {
	
	private String device;
	private String loginSecretKey;
	private String loginSignKey;
	private String loginTimestamp;
	private String userInfo;
	
	public String getDevice() {
		return device;
	}
	public void setDevice(String device) {
		this.device = device;
	}
	public String getLoginSecretKey() {
		return loginSecretKey;
	}
	public void setLoginSecretKey(String loginSecretKey) {
		this.loginSecretKey = loginSecretKey;
	}
	public String getLoginSignKey() {
		return loginSignKey;
	}
	public void setLoginSignKey(String loginSignKey) {
		this.loginSignKey = loginSignKey;
	}
	public String getLoginTimestamp() {
		return loginTimestamp;
	}
	public void setLoginTimestamp(String loginTimestamp) {
		this.loginTimestamp = loginTimestamp;
	}
	public String getUserInfo() {
		return userInfo;
	}
	public void setUserInfo(String userInfo) {
		this.userInfo = userInfo;
	}
	
	
	
}
